<?php
function somme_nombre ($A,$B){
    return $A+$B ;
}
function difference_nombre ($A,$B){
    return $A-$B;
}
function produit_nombre($A,$B){
    return $A*$B;
}
function division_nombre ($A,$B){
    return $A/$B;
}
function modulo_nombre($A,$B){
    return $A%$B;
}
function carre_nombre($A){
    return pow($A,2);   
}
function exponentiel_nombre($A){
    return exp($A);
}
?>